	BOARD_FILE	VARCHAR2(50),
	BOARD_RE_REF	NUMBER,
	BOARD_RE_LEV	NUMBER,
	BOARD_RE_SEQ	NUMBER,
	BOARD_READCOUNT	NUMBER,
	BOARD_DATE	DATE,
